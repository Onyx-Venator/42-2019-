#include <push_swap.h>

int	ft_atoi(char const *s)
{
	int	i;
	int	n;
	int	res;

	i = 0;
	res = 0;
	n = 1;
	while ((s[i] >= '\t' && s[i] <= '\r') || s[i] == ' ')
		i++;
	if (s[i] == '+' || s[i] == '-')
	{
		if (s[i] == '-')
			n = n * -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		res = res * 10 + (s[i] - '0');
		i++;
	}
	return (res * n);
}
