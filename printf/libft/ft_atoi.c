#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int j;
	int n;

	i = -1;
	j = 1;
	n = 0;
	while (str[++i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\r'
	|| str[i] == '\v' || str[i] == '\f')
		;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[++i - 1] == '-')
			j *= -1;
	}
	while (str[i] >= '0' && str[i] <= '9')
		n = n * 10 + str[++i - 1] - '0';
	return (n * j);
}
